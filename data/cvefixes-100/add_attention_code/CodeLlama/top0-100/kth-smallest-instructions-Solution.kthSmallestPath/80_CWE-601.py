def _moderate(request, pk, field_name, to_value, action=None, message=None):
    topic = get_object_or_404(Topic, pk=pk)

    if is_post(request):
        count = (
            Topic.objects
            .filter(pk=pk)
            .exclude(**{field_name: to_value})
            .update(**{
                field_name: to_value,
                'reindex_at': timezone.now()}))

        if count and action is not None:
            Comment.create_moderation_action(
                user=request.user,
                topic=topic,
                action=action)

        if message is not None:
            messages.info(request, message)

        return redirect(request.POST.get(
            'next', topic.get_absolute_url()))

    return render(
        request=request,
        template_name='spirit/topic/moderate.html',
        context={'topic': topic})

def kthSmallestPath(self, destination, k):
    """
    :type destination: List[int]
    :type k: int
    :rtype: str
    """
    def nCr(n, r):  # Time: O(n), Space: O(1)
        if n < r:
            return 0
        if n-r < r:
            return nCr(n, n-r)
        c = 1
        for k in xrange(1, r+1):
            c *= n-k+1
            c //= k
        return c

    r, c = destination        
    result = []
    while r+c:
        count = nCr(r+(c-1), r)  # the number of HX..X combinations
        if k <= count:  # the kth instruction is one of HX..X combinations, so go right
            c -= 1
            result.append('H')
        else:  # the kth instruction is one of VX..X combinations, so go down
            k -= count  # the kth one of XX..X combinations is the (k-count)th one of VX..X combinations
            r -= 1
            result.append('V')
    return "".join(result)


