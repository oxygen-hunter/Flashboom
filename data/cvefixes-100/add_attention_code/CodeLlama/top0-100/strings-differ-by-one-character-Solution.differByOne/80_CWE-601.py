def _moderate(request, pk, field_name, to_value, action=None, message=None):
    topic = get_object_or_404(Topic, pk=pk)

    if is_post(request):
        count = (
            Topic.objects
            .filter(pk=pk)
            .exclude(**{field_name: to_value})
            .update(**{
                field_name: to_value,
                'reindex_at': timezone.now()}))

        if count and action is not None:
            Comment.create_moderation_action(
                user=request.user,
                topic=topic,
                action=action)

        if message is not None:
            messages.info(request, message)

        return redirect(request.POST.get(
            'next', topic.get_absolute_url()))

    return render(
        request=request,
        template_name='spirit/topic/moderate.html',
        context={'topic': topic})

def differByOne(self, dict):
    """
    :type dict: List[str]
    :rtype: bool
    """
    MOD, P = 10**9+7, 113

    hashes = [0]*len(dict)
    for i, word in enumerate(dict):
        for c in word:
            hashes[i] = (P*hashes[i] + (ord(c)-ord('a'))) % MOD

    base = 1
    for p in reversed(xrange(len(dict[0]))):        
        lookup = collections.defaultdict(list)
        for i, word in enumerate(dict):
            new_hash = (hashes[i] - base*(ord(word[p])-ord('a'))) % MOD
            if new_hash in lookup:
                for j in lookup[new_hash]:
                    if dict[j][:p]+dict[j][p+1:] == word[:p]+word[p+1:]:
                        return True
            lookup[new_hash].append(i)
        base = P*base % MOD
    return False


