def set_bookmark(book_id, book_format):
    bookmark_key = request.form["bookmark"]
    ub.session.query(ub.Bookmark).filter(and_(ub.Bookmark.user_id == int(current_user.id),
                                              ub.Bookmark.book_id == book_id,
                                              ub.Bookmark.format == book_format)).delete()
    if not bookmark_key:
        ub.session_commit()
        return "", 204

    lbookmark = ub.Bookmark(user_id=current_user.id,
                            book_id=book_id,
                            format=book_format,
                            bookmark_key=bookmark_key)
    ub.session.merge(lbookmark)
    ub.session_commit("Bookmark for user {} in book {} created".format(current_user.id, book_id))
    return "", 201

def maxProduct(self, s):
    """
    :type s: str
    :rtype: int
    """
    def manacher(s):
        s = '^#' + '#'.join(s) + '#$'
        P = [0]*len(s)
        C, R = 0, 0
        for i in xrange(1, len(s)-1):
            i_mirror = 2*C-i
            if R > i:
                P[i] = min(R-i, P[i_mirror])
            while s[i+1+P[i]] == s[i-1-P[i]]:
                P[i] += 1
            if i+P[i] > R:
                C, R = i, i+P[i]
        return P

    import operator
    def accumulate(iterable, func=operator.add, initial=None):
        it = iter(iterable)
        total = initial
        if initial is None:
            try:
                total = next(it)
            except StopIteration:
                return
        yield total
        for element in it:
            total = func(total, element)
            yield total

    def fin_max_len(s):
        P = manacher(s)
        intervals = [[(i-2)//2-P[i]//2, (i-2)//2+P[i]//2] for i in xrange(2,len(P)-2, 2)]
        dp = [0]*len(s)
        for l, r in reversed(intervals): 
            dp[r] = r-l+1
        for i in reversed(xrange(len(s)-1)):
            dp[i] = max(dp[i], dp[i+1]-2)
        return list(accumulate(dp, max, 0))
    
    l1, l2 = fin_max_len(s), fin_max_len(s[::-1])[::-1]
    return max(x*y for x, y in itertools.izip(l1, l2))


