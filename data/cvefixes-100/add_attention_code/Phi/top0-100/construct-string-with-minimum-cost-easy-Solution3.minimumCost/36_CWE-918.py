def ratings_list():
    if current_user.check_visibility(constants.SIDEBAR_RATING):
        if current_user.get_view_property('ratings', 'dir') == 'desc':
            order = db.Ratings.rating.desc()
            order_no = 0
        else:
            order = db.Ratings.rating.asc()
            order_no = 1
        entries = calibre_db.session.query(db.Ratings, func.count('books_ratings_link.book').label('count'),
                                   (db.Ratings.rating / 2).label('name')) \
            .join(db.books_ratings_link).join(db.Books).filter(calibre_db.common_filters()) \
            .group_by(text('books_ratings_link.rating')).order_by(order).all()
        return render_title_template('list.html', entries=entries, folder='web.books_list', charlist=list(),
                                     title=_(u"Ratings list"), page="ratingslist", data="ratings", order=order_no)
    else:
        abort(404)

def minimumCost(self, target, words, costs):
    """
    :type target: str
    :type words: List[str]
    :type costs: List[int]
    :rtype: int
    """
    INF = float("inf")
    class Trie(object):
        def __init__(self):
            self.__nodes = []
            self.__mns = []
            self.__new_node()
        
        def __new_node(self):
            self.__nodes.append([-1]*26)
            self.__mns.append(INF)
            return len(self.__nodes)-1

        def add(self, w, c):
            curr = 0
            for x in w:
                x = ord(x)-ord('a')
                if self.__nodes[curr][x] == -1:
                    self.__nodes[curr][x] = self.__new_node()
                curr = self.__nodes[curr][x]
            self.__mns[curr] = min(self.__mns[curr], c)
        
        def query(self, i):
            curr = 0
            for j in xrange(i, len(target)):
                x = ord(target[j])-ord('a')
                if self.__nodes[curr][x] == -1:
                    break
                curr = self.__nodes[curr][x]
                if self.__mns[curr] != INF:
                    dp[j+1] = min(dp[j+1], dp[i]+self.__mns[curr])

    trie = Trie()
    for w, c in itertools.izip(words, costs):
        trie.add(w, c)
    dp = [INF]*(len(target)+1)
    dp[0] = 0
    for i in xrange(len(target)):
        if dp[i] == INF:
            continue
        trie.query(i)
    return dp[-1] if dp[-1] != INF else -1


