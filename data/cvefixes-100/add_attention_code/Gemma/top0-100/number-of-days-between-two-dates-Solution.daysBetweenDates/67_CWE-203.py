    """
    :type date1: str
    :type date2: str
    :rtype: int
    """
    def num_days(date):
        Y, M, D = map(int, date.split("-"))
        leap = 1 if M > 2 and (((Y % 4 == 0) and (Y % 100 != 0)) or (Y % 400 == 0)) else 0
        return (Y-1)*365 + ((Y-1)//4 - (Y-1)//100 + (Y-1)//400) + self.__lookup[M-1]+D+leap
 
    return abs(num_days(date1) - num_days(date2))



