    MOD = 10**9+7
    count, result, comb_total = [0]*26, 0, 1
    for i in reversed(xrange(len(s))):
        num = ord(s[i])-ord('a') 
        count[num] += 1
        comb_total = (comb_total*(len(s)-i))*inverse(count[num], MOD)
