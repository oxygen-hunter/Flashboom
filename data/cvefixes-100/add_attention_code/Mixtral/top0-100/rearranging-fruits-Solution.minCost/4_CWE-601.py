    cnt = collections.Counter()
    for x in basket1:
        cnt[x] += 1
    for x in basket2:
        cnt[x] -= 1
    mn = min(cnt.iterkeys())
    swaps = []
    for k, v in cnt.iteritems():
        if v%2:
            return -1
        swaps.extend(k for _ in xrange(abs(v)//2))
    nth_element(swaps, len(swaps)//2)
