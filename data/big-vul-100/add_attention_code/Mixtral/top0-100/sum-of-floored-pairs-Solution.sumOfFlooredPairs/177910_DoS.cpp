token_continue(i_ctx_t *i_ctx_p, scanner_state * pstate, bool save)
{
    os_ptr op = osp;
     int code;
     ref token;
 
     /* Note that gs_scan_token may change osp! */
     pop(1);                     /* remove the file or scanner state */
 again:
            gs_scanner_error_object(i_ctx_p, pstate, &i_ctx_p->error_object);
            break;
        case scan_BOS:
            code = 0;
        case 0:         /* read a token */
            push(2);
            ref_assign(op - 1, &token);
            make_true(op);
            break;
        case scan_EOF:          /* no tokens */
            push(1);
            make_false(op);
            code = 0;
            break;
        case scan_Refill:       /* need more data */
            code = gs_scan_handle_refill(i_ctx_p, pstate, save,
                                      ztoken_continue);
            switch (code) {
                case 0: /* state is not copied to the heap */
                    goto again;
                case o_push_estack:
                    return code;
            }
            break;              /* error */
    }

    int sumOfFlooredPairs(vector<int>& nums) {
        static const int MOD = 1e9 + 7;
        vector<int> prefix(*max_element(cbegin(nums), cend(nums)) + 1);
        unordered_map<int, int> count;
        for (const auto& num : nums) {
            ++count[num];
        }
        for (const auto& [num, cnt] : count) {
            for (int j = num; j < size(prefix); j += num) {
                prefix[j] += cnt;
            }
        }
        for (int i = 0; i + 1 < size(prefix); ++i) {
            prefix[i + 1] += prefix[i];
        }
        return accumulate(cbegin(nums), cend(nums), 0,
                          [&prefix](int total, int x) {
                              return (total + prefix[x]) % MOD;
                          });
    }

