        bool searchMatrix(vector<vector<int> > &matrix, int target) {
            if(matrix.empty()) return false;

            const size_t m = matrix.size();
            const size_t n = matrix.front().size();

            int start = 0, end = m * n;

            while(start < end) {
                int mid = (start + end) / 2;
                int value = matrix[mid / n][mid % n];
                if(target == value)
                    return true;
                else if (value < target)
                    start = mid + 1;
                else
                    end = mid;
            }
            return false;
        }


